package api;

public class Article {
	
	private int id;
	private String name;
	private String description;
	private double price;
	
	public int getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public String getDescription() {
		return description;
	}
	
	public double getPrice() {
		return price;
	}
	
	

}
